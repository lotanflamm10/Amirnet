// ── helpers ──────────────────────────────────────────────────────────────────

function normalize(word) {
  return word.replace(/^to\s+/i, "").toLowerCase().trim();
}

function guessPos(word) {
  const w = word.toLowerCase().trim();
  if (w.startsWith("to ")) return "verb";
  if (/tion$|sion$|ness$|ment$|ity$|ance$|ence$|ism$|ship$|age$|ure$|or$|er$|ist$|ry$|cy$|ty$/.test(w)) return "noun";
  if (/ful$|less$|ous$|ive$|ic$|al$|ary$|ible$|able$|ent$|ant$/.test(w)) return "adjective";
  if (/ly$/.test(w)) return "adverb";
  return "noun";
}

function guessCategory(difficulty, word) {
  const w = word.toLowerCase();
  if (difficulty === "hard") {
    if (w.startsWith("to ")) return "academic verbs";
    return "abstract nouns";
  }
  if (difficulty === "medium") {
    if (w.startsWith("to ")) return "academic verbs";
    return "basic vocabulary";
  }
  return "basic vocabulary";
}

function makeId(word) {
  return "noam_b1_" + normalize(word).replace(/[^a-z0-9]/g, "_");
}

function makeItem(word, hebrew, difficulty) {
  const nw = normalize(word);
  return {
    id: makeId(word),
    word: word.trim(),
    normalizedWord: nw,
    hebrewTranslation: hebrew.trim(),
    englishDefinition: null,
    partOfSpeech: guessPos(word),
    difficulty,
    category: guessCategory(difficulty, word),
    exampleSentence: null,
    exampleSentenceHebrew: null,
    synonyms: [],
    antonyms: [],
    confusingWords: [],
    commonTrap: null,
    tags: ["noam-batch1"],
    source: "noam_machon_batch1",
    originalLine: `${word} – ${hebrew}`,
    needsReview: false,
    studyPriority: difficulty === "easy" ? 3 : difficulty === "medium" ? 5 : 7,
    createdAt: NOW,
    updatedAt: NOW,
  };
}

